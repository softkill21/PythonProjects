#!/bin/bash
input="dates.txt"
#[\s][0-9][0-9][\s][0-9][0-9]:[0-9][0-9]:[0-9][0-9] 
#*[A-Z][A-Z][A-Z]* 
while IFS= read -r line
do
  echo "$line"
	if 
[[ $line =~ [A-Z][a-z][a-z][' '][3-9][2-9][' '][0-5][0-9][':'][2-9][5-9][':'][0-5][0-9]
|| $line =~ [A-Z][a-z][a-z][' '][3-9][2-9][' '][0-5][0-9][':'][2-9][0-9][':'][0-5][0-9]
|| $line =~ ["Feb"][' '][3][0-9][' '][0-5][0-9][':'][0-5][0-9][':'][0-5][0-9] 
|| $line =~ ["Apr"][' '][3][1][' '][0-5][0-9][':'][0-5][0-9][':'][0-5][0-9] 
|| $line =~ ["Jun"][' '][3][1][' '][0-5][0-9][':'][0-5][0-9][':'][0-5][0-9] 
|| $line =~ ["Sep"][' '][3][1][' '][0-5][0-9][':'][0-5][0-9][':'][0-5][0-9] 
|| $line =~ ["Nov"][' '][3][1][' '][0-5][0-9][':'][0-5][0-9][':'][0-5][0-9]  ]]
	then	echo "FFormato incorrecto"

	elif 
[[ $line =~ [A-Z][a-z][a-z][' '][1-2][0-9][' '][0-5][0-9][':'][0-5][0-9][':'][0-5][0-9]  || $line =~ [A-Z][a-z][a-z][' '][' '][1-9][' '][0-5][0-9][':'][0-5][0-9][':'][0-5][0-9] || $line =~ [A-Z][a-z][a-z][' '][3][0-1][' '][0-2][0-4][':'][0-5][0-9][':'][0-5][0-9]  ]]
	then	echo "correcto"

	else
		echo "Formato incorrecto"
	fi

done < "$input"




